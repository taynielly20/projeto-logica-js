/*Crie um algoritmo que calcule a média de 3 notas e exiba o resultado.*/

// Três notas
let nota1 = 10;
let nota2 = 30;
let nota3 = 50;

// Calcular a média das três notas
let media = (nota1 + nota2 + nota3) / 3;

// Exibir o resultado
console.log("A média das três notas é: " + media.toFixed(2));

// Exibimos o resultado da média com duas casas decimais usando o método toFixed().