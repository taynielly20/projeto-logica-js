/**
 * Crie uma variável chamada "numero" e atribua a ela um número qualquer. 
 * Verifique se o número é positivo, negativo ou zero utilizando uma estrutura condicional (if-else) e exiba a mensagem no console.
 */

let numero = 0;

if (numero > 0) {
  console.log("O número é positivo.");
} else if (numero < 0) {
  console.log("O número é negativo.");
} else {
  console.log("O número é zero.");
}